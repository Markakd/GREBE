#include <immintrin.h>

volatile __m512 z;
volatile __m256 y;
volatile __m128 x;
volatile __mmask8 n;
volatile __mmask16 m;

void extern
avx512dq_test (void)
{
  z = _mm512_or_ps (z, z);
  z = _mm512_mask_or_ps (z, m, z, z);
  z = _mm512_maskz_or_ps (m, z, z);

  y = _mm256_mask_or_ps (y, n, y, y);
  y = _mm256_maskz_or_ps (n, y, y);

  x = _mm_mask_or_ps (x, n, x, x);
  x = _mm_maskz_or_ps (n, x, x);
}
